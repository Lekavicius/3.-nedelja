// FOROF
{
    let niz = [1,2,3,4]
    for (const el of niz) {
        console.log(el);
    }
// FOROF je zgodan za prolazak kroz niz, kada treba da prodjemo kroz sve elemente niza.
}

// FORIN

{
    let  obj = {x:5, y:8}
    for (const key in obj) {
        console.log(obj[key]);
    }
// FORIN je zgodan za prolazak kroz objekat  

}

